package RefrenceMethod.StaticMethod;

public class MathUtil {
    public static int minus(int a,int b)
    {
        return a-b;
    }
    public static int add(int a,int b)
    {
        return a+b;
    }
    public static int multiply(int a,int b)
    {
        return a*b;
    }
    public static  int divided(int a,int b)
    {
        return a/b;
    }
}
